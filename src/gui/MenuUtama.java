    private void loadBrg(List<CrudEdit> brgList) {
        DefaultTableModel model = (DefaultTableModel) taBarang.getModel();
        model.setRowCount(0); // Clear existing data
        if (brgList.isEmpty()){
            model.addRow(new Object[]{"Tidak dapat ditemukan", "", "", "", "", ""});
        }else{
            for (CrudEdit brg : brgList) {
                Object[] row = {
                    brg.getIdBrg(),
                    brg.getNama(),
                    brg.getTgl(),
                    brg.getKategori(),
                    brg.getJml(),
                    brg.getIdPmsk()
                };
                model.addRow(row);
            }
        }
    }
