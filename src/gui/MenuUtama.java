    private void bSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSearchActionPerformed
        // TODO add your handling code here:
        String searchTerm = tSearch.getText().toLowerCase();
        // Mengecek apakah user memasukkan 2 karakter
        if (searchTerm.length() < 2) {
            javax.swing.JOptionPane.showMessageDialog(this, "Masukkan minimal 2 huruf untuk pencarian.", "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Utama utama = new Utama();
        List<CrudEdit> brgList = CrudEdit.getAllBrg(); 

        // Filter berdasarkan ID Barang atau Nama Barang
        List<CrudEdit> filteredList = brgList.stream()
                .filter(brg -> brg.getIdBrg().toLowerCase().contains(searchTerm) || 
                               brg.getNama().toLowerCase().contains(searchTerm))
                .collect(Collectors.toList());

        loadBrg(filteredList);
    }//GEN-LAST:event_bSearchActionPerformed

